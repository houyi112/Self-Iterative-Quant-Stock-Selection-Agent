def _load_json(filename: str, default: dict) -> dict:
    """从 data/ 目录加载 JSON 配置文件，不存在则返回默认值。"""
    import json
    path = PROJECT_ROOT / "data" / filename
    if path.exists():
        try:
            return json.loads(path.read_text(encoding="utf-8"))
        except (json.JSONDecodeError, OSError):
            pass
    return default

